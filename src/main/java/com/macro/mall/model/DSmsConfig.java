package com.macro.mall.model;


public class DSmsConfig {
    private String id;

    private String name;

    private String content;

    private String loginFlag;

    private String cardFlag;

    private String idtypeFlag;

    private String phoneFlag;

    private String state;

    /**
     * @return ID
     */
    public String getId() {
        return id;
    }

    /**
     * @param id
     */
    public void setId(String id) {
        this.id = id;
    }

    /**
     * @return NAME
     */
    public String getName() {
        return name;
    }

    /**
     * @param name
     */
    public void setName(String name) {
        this.name = name;
    }

    /**
     * @return CONTENT
     */
    public String getContent() {
        return content;
    }

    /**
     * @param content
     */
    public void setContent(String content) {
        this.content = content;
    }

    /**
     * @return LOGIN_FLAG
     */
    public String getLoginFlag() {
        return loginFlag;
    }

    /**
     * @param loginFlag
     */
    public void setLoginFlag(String loginFlag) {
        this.loginFlag = loginFlag;
    }

    /**
     * @return CARD_FLAG
     */
    public String getCardFlag() {
        return cardFlag;
    }

    /**
     * @param cardFlag
     */
    public void setCardFlag(String cardFlag) {
        this.cardFlag = cardFlag;
    }

    /**
     * @return IDTYPE_FLAG
     */
    public String getIdtypeFlag() {
        return idtypeFlag;
    }

    /**
     * @param idtypeFlag
     */
    public void setIdtypeFlag(String idtypeFlag) {
        this.idtypeFlag = idtypeFlag;
    }

    /**
     * @return PHONE_FLAG
     */
    public String getPhoneFlag() {
        return phoneFlag;
    }

    /**
     * @param phoneFlag
     */
    public void setPhoneFlag(String phoneFlag) {
        this.phoneFlag = phoneFlag;
    }

    /**
     * @return STATE
     */
    public String getState() {
        return state;
    }

    /**
     * @param state
     */
    public void setState(String state) {
        this.state = state;
    }
}